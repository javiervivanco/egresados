-- =============================================================
-- RLS
-- =============================================================
-- Estrategia:
--   * Tablas de catálogo público (escuelas, empresas, destinos, planes_viaje):
--     lectura pública (anon), escritura solo por roles autorizados.
--   * Tablas de familia (familias, alumnos, votos_*):
--     escritura/lectura por la propia familia o por admins.
--   * profiles: cada user lee el suyo; super_admin lee todos.

-- Helper: ¿el user actual es super_admin?
create or replace function is_super_admin() returns boolean
  language sql stable security definer
  as $$ select exists (select 1 from profiles where user_id = auth.uid() and rol = 'super_admin') $$;

-- Helper: ¿el user actual es admin de la empresa pasada?
create or replace function is_empresa_admin(p_empresa_id bigint) returns boolean
  language sql stable security definer
  as $$ select exists (
    select 1 from profiles
    where user_id = auth.uid() and rol = 'empresa_admin' and empresa_id = p_empresa_id
  ) $$;

-- Helper: familia del user actual (devuelve familia_id o null)
create or replace function current_familia_id() returns bigint
  language sql stable security definer
  as $$ select familia_id from profiles where user_id = auth.uid() $$;

alter table profiles         enable row level security;
alter table escuelas         enable row level security;
alter table grupos           enable row level security;
alter table familias         enable row level security;
alter table alumnos          enable row level security;
alter table empresas         enable row level security;
alter table destinos         enable row level security;
alter table planes_viaje     enable row level security;
alter table documentos       enable row level security;
alter table fechas_reunion   enable row level security;
alter table votos_fecha      enable row level security;
alter table votos_plan       enable row level security;
alter table correcciones     enable row level security;
alter table planes           enable row level security;
alter table votos            enable row level security;

-- profiles: leo el mío; super_admin lee todos.
create policy "profile self read" on profiles for select using (user_id = auth.uid() or is_super_admin());
create policy "profile self update" on profiles for update using (user_id = auth.uid());
create policy "profile super_admin all" on profiles for all using (is_super_admin()) with check (is_super_admin());

-- Catálogos públicos (read anon ok)
create policy "escuelas read" on escuelas for select to anon, authenticated using (true);
create policy "empresas read" on empresas for select to anon, authenticated using (true);
create policy "destinos read" on destinos for select to anon, authenticated using (true);
create policy "planes_viaje read" on planes_viaje for select to anon, authenticated using (true);
create policy "documentos read" on documentos for select to authenticated using (true);

-- Mutación de empresas: super_admin
create policy "empresas write super" on empresas for all using (is_super_admin()) with check (is_super_admin());
create policy "escuelas write super" on escuelas for all using (is_super_admin()) with check (is_super_admin());

-- Mutación de destinos/planes/documentos: empresa_admin de la empresa dueña, o super
create policy "destinos write owner" on destinos for all
  using (is_super_admin() or is_empresa_admin(empresa_id))
  with check (is_super_admin() or is_empresa_admin(empresa_id));

create policy "planes_viaje write owner" on planes_viaje for all
  using (is_super_admin() or is_empresa_admin((select empresa_id from destinos where id = destino_id)))
  with check (is_super_admin() or is_empresa_admin((select empresa_id from destinos where id = destino_id)));

create policy "documentos write owner" on documentos for all
  using (
    is_super_admin() or
    (destino_id is not null and is_empresa_admin((select empresa_id from destinos where id = destino_id))) or
    (plan_id    is not null and is_empresa_admin((select empresa_id from destinos d join planes_viaje p on p.destino_id = d.id where p.id = plan_id)))
  ) with check (true);

-- Grupos / familias / alumnos: lectura por miembros del grupo, super_admin, y por empresas
-- (las empresas necesitan ver qué grupos están interesados — refinar en fase 2).
create policy "grupos read" on grupos for select using (true);
create policy "grupos write super" on grupos for all using (is_super_admin()) with check (is_super_admin());

create policy "familias read" on familias for select using (true);
create policy "familias insert public" on familias for insert to anon, authenticated with check (true);
create policy "familias update self" on familias for update using (
  is_super_admin() or id = current_familia_id()
);

create policy "alumnos read" on alumnos for select using (true);
create policy "alumnos write owner" on alumnos for all using (
  is_super_admin() or familia_id = current_familia_id()
) with check (
  is_super_admin() or familia_id = current_familia_id()
);

-- Fechas reunión: lectura para grupo y empresa relacionados; escritura por empresa o super.
create policy "fechas read" on fechas_reunion for select using (true);
create policy "fechas write empresa" on fechas_reunion for all
  using (is_super_admin() or is_empresa_admin(empresa_id))
  with check (is_super_admin() or is_empresa_admin(empresa_id));

create policy "votos_fecha read" on votos_fecha for select using (true);
create policy "votos_fecha write self" on votos_fecha for all
  using (is_super_admin() or familia_id = current_familia_id())
  with check (is_super_admin() or familia_id = current_familia_id());

create policy "votos_plan read" on votos_plan for select using (true);
create policy "votos_plan write self" on votos_plan for all
  using (is_super_admin() or familia_id = current_familia_id())
  with check (is_super_admin() or familia_id = current_familia_id());

create policy "correcciones insert any" on correcciones for insert to anon, authenticated with check (true);
create policy "correcciones read" on correcciones for select using (true);
create policy "correcciones update admin" on correcciones for update
  using (is_super_admin() or (plan_id is not null and is_empresa_admin((select empresa_id from destinos d join planes_viaje p on p.destino_id = d.id where p.id = plan_id))));

-- Legacy (mantener abierto mientras conviva con el frontend público actual)
create policy "planes legacy read" on planes for select to anon, authenticated using (true);
create policy "votos legacy insert" on votos for insert to anon, authenticated with check (true);
create policy "votos legacy read" on votos for select to anon, authenticated using (true);

-- =============================================================
-- Storage: bucket para documentos de viaje
-- =============================================================
insert into storage.buckets (id, name, public) values ('documentos', 'documentos', false)
  on conflict (id) do nothing;

-- RLS de storage.objects: la dueña empresa puede leer/escribir sus documentos;
-- familias autenticadas pueden leer (descargar).
create policy "documentos storage read auth" on storage.objects for select
  to authenticated using (bucket_id = 'documentos');

create policy "documentos storage write empresa" on storage.objects for insert
  to authenticated with check (bucket_id = 'documentos');

create policy "documentos storage delete owner" on storage.objects for delete
  to authenticated using (bucket_id = 'documentos' and owner = auth.uid());
