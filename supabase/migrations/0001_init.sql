-- =============================================================
-- Egresados — schema inicial
-- =============================================================
-- Conceptos:
--   * Una ESCUELA agrupa GRUPOS (un grupo = un grado/año de egreso).
--   * Cada GRUPO contiene FAMILIAS, y cada FAMILIA puede tener uno o más ALUMNOS.
--   * Las EMPRESAS publican DESTINOS, cada destino tiene PLANES_VIAJE
--     (combinaciones de duración/transporte/forma de pago) y DOCUMENTOS adjuntos
--     que en una fase posterior van a ser procesados por IA.
--   * El flujo de votación es en dos etapas:
--       1) FECHAS_REUNION — la empresa propone fechas y las familias eligen.
--       2) VOTOS_PLAN — una vez vista la presentación, las familias rankean planes.
--   * PROFILES vincula auth.users (Supabase Auth) a su rol y a la entidad
--     correspondiente (empresa o familia).
-- =============================================================

-- =============================================================
-- Roles / Profiles
-- =============================================================
create type user_rol as enum ('super_admin', 'empresa_admin', 'familia');

create table profiles (
  user_id      uuid primary key references auth.users(id) on delete cascade,
  rol          user_rol not null,
  nombre       text,
  empresa_id   bigint, -- FK seteada más abajo (forward ref)
  familia_id   bigint, -- FK seteada más abajo
  created_at   timestamptz default now()
);

-- =============================================================
-- Escuelas / Grupos / Familias / Alumnos
-- =============================================================
create table escuelas (
  id          bigint generated by default as identity primary key,
  nombre      text not null,
  localidad   text,
  provincia   text,
  created_at  timestamptz default now()
);

create table grupos (
  id            bigint generated by default as identity primary key,
  escuela_id    bigint not null references escuelas(id) on delete cascade,
  anio_egreso   smallint not null,                -- ej: 2026
  grado         text,                             -- "6to A", "7mo B", "Secundario 5to"
  estado        text default 'activo',            -- activo | cerrado
  created_at    timestamptz default now(),
  unique (escuela_id, anio_egreso, grado)
);

create table familias (
  id          bigint generated by default as identity primary key,
  grupo_id    bigint not null references grupos(id) on delete cascade,
  apellido    text not null,
  email       text,
  telefono    text,
  created_at  timestamptz default now()
);

create table alumnos (
  id          bigint generated by default as identity primary key,
  familia_id  bigint not null references familias(id) on delete cascade,
  nombre      text not null,
  created_at  timestamptz default now()
);

alter table profiles add constraint profiles_familia_fk
  foreign key (familia_id) references familias(id) on delete set null;

-- =============================================================
-- Empresas / Destinos / Planes / Documentos
-- =============================================================
create table empresas (
  id              bigint generated by default as identity primary key,
  nombre          text not null,
  slug            text unique not null,
  contacto_email  text,
  contacto_tel    text,
  activo          boolean default true,
  created_at      timestamptz default now()
);

alter table profiles add constraint profiles_empresa_fk
  foreign key (empresa_id) references empresas(id) on delete cascade;

create table destinos (
  id          bigint generated by default as identity primary key,
  empresa_id  bigint not null references empresas(id) on delete cascade,
  nombre      text not null,                  -- "Carlos Paz / Córdoba"
  provincia   text,
  descripcion text,
  activo      boolean default true,
  created_at  timestamptz default now()
);

create table planes_viaje (
  id                bigint generated by default as identity primary key,
  destino_id        bigint not null references destinos(id) on delete cascade,
  transporte        text,                     -- "Bus" | "Avión"
  dias              smallint,
  noches            smallint,
  plan_pago         text,                     -- nombre del plan
  cantidad_cuotas   smallint,
  inscripcion       integer,
  reserva           integer,
  primera_cuota     integer,
  cuota_mensual     integer,
  anticipo_saldo    integer,
  total_final       integer,
  liberados         text,
  seguro            text,
  descuentos        text,
  actividades       text,
  vigencia          text,
  activo            boolean default true,
  created_at        timestamptz default now()
);
create index on planes_viaje (destino_id);

-- Documentos subidos por la empresa (PDFs, fotos, planillas).
-- procesado_estado controla el pipeline futuro de IA:
--   pendiente → procesando → procesado | error
-- datos_extraidos guarda el output estructurado del LLM (mock por ahora).
create table documentos (
  id                  bigint generated by default as identity primary key,
  destino_id          bigint references destinos(id) on delete cascade,
  plan_id             bigint references planes_viaje(id) on delete cascade,
  nombre              text not null,
  storage_path        text not null,          -- path en bucket "documentos"
  mime_type           text,
  size_bytes          bigint,
  procesado_estado    text default 'pendiente',
  datos_extraidos     jsonb,
  procesado_at        timestamptz,
  uploaded_by         uuid references auth.users(id),
  created_at          timestamptz default now(),
  check (destino_id is not null or plan_id is not null)
);
create index on documentos (destino_id);
create index on documentos (plan_id);
create index on documentos (procesado_estado);

-- =============================================================
-- Votación etapa 1: fechas de reunión
-- =============================================================
create table fechas_reunion (
  id           bigint generated by default as identity primary key,
  grupo_id     bigint not null references grupos(id) on delete cascade,
  empresa_id   bigint not null references empresas(id) on delete cascade,
  fecha        timestamptz not null,
  ubicacion    text,                          -- presencial, link, etc
  estado       text default 'propuesta',      -- propuesta | confirmada | cancelada | realizada
  created_by   uuid references auth.users(id),
  created_at   timestamptz default now()
);
create index on fechas_reunion (grupo_id);

create table votos_fecha (
  familia_id  bigint not null references familias(id) on delete cascade,
  fecha_id    bigint not null references fechas_reunion(id) on delete cascade,
  voto        boolean not null,               -- true=acepta, false=rechaza
  created_at  timestamptz default now(),
  primary key (familia_id, fecha_id)
);

-- =============================================================
-- Votación etapa 2: planes
-- =============================================================
-- Una familia rankea hasta 3 planes (prioridad 1/2/3).
create table votos_plan (
  id          bigint generated by default as identity primary key,
  familia_id  bigint not null references familias(id) on delete cascade,
  plan_id     bigint not null references planes_viaje(id) on delete cascade,
  prioridad   smallint not null check (prioridad between 1 and 3),
  comentario  text,
  created_at  timestamptz default now(),
  unique (familia_id, prioridad)
);

-- =============================================================
-- Correcciones (reportes de errores en los datos cargados)
-- =============================================================
create table correcciones (
  id              bigint generated by default as identity primary key,
  plan_id         bigint references planes_viaje(id) on delete cascade,
  destino_id      bigint references destinos(id) on delete cascade,
  campo           text not null,
  valor_actual    text,
  valor_correcto  text,
  comentario      text,
  reportado_by    uuid references auth.users(id),
  estado          text default 'pendiente',
  created_at      timestamptz default now()
);

