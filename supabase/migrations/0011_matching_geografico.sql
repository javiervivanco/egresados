alter table ciudades enable row level security;
create policy "ciudades public read" on ciudades for select using (true);
create policy "ciudades super write" on ciudades for all using (is_super_admin()) with check (is_super_admin());

-- =============================================================
-- Orígenes de operación de cada empresa
-- =============================================================
create table if not exists empresas_origenes (
  empresa_id  bigint not null references empresas(id) on delete cascade,
  ciudad_id   bigint not null references ciudades(id) on delete restrict,
  created_at  timestamptz default now(),
  primary key (empresa_id, ciudad_id)
);
create index if not exists empresas_origenes_ciudad_idx on empresas_origenes(ciudad_id);

alter table empresas_origenes enable row level security;
create policy "empresas_origenes public read" on empresas_origenes for select using (true);
create policy "empresas_origenes super write" on empresas_origenes for all
  using (is_super_admin()) with check (is_super_admin());
create policy "empresas_origenes empresa write" on empresas_origenes for all
  using (is_empresa_admin(empresa_id)) with check (is_empresa_admin(empresa_id));

-- =============================================================
-- Escuelas tienen ciudad
-- =============================================================
alter table escuelas
  add column if not exists ciudad_id bigint references ciudades(id) on delete set null;
create index if not exists escuelas_ciudad_idx on escuelas(ciudad_id);

-- Backfill best-effort: match localidad (case-insensitive) contra ciudades.nombre.
update escuelas e
   set ciudad_id = c.id
  from ciudades c
 where e.ciudad_id is null
   and e.localidad is not null
   and lower(btrim(e.localidad)) = lower(btrim(c.nombre));

-- =============================================================
-- Helpers públicos
-- =============================================================

-- empresas_visibles_para_ciudad(ciudad_id) → empresas que operan desde esa ciudad.
-- Útil para listados; el frontend lo invoca via PostgREST.
create or replace view empresas_visibles_por_ciudad as
  select co.ciudad_id, e.*
    from empresas e
    join empresas_origenes co on co.empresa_id = e.id;

-- planes_relevantes(ciudad_id) → filtro definitivo: planes de empresas
-- que operan desde la ciudad pasada. Si no hay ciudad → devuelve todo
-- (modo legacy / admin sin filtro).
create or replace function planes_relevantes(p_ciudad_id bigint default null)
returns setof planes_viaje
  language sql stable
as $$
  select p.* from planes_viaje p
  where p_ciudad_id is null
     or exists (
       select 1 from empresas_origenes co
       join destinos d on d.empresa_id = co.empresa_id
       where co.ciudad_id = p_ciudad_id
         and p.destino_id = d.id
     );
$$;

grant execute on function planes_relevantes(bigint) to anon, authenticated;
