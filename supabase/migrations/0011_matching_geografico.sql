-- =============================================================
-- Matching geográfico: ciudad de salida ↔ empresa que opera ahí
-- =============================================================
-- Una familia ve solo las empresas que efectivamente operan desde su
-- ciudad. Sin este filtro la comparativa es ruido (una familia en Mendoza
-- ve viajes que salen de Buenos Aires y no puede tomar).
--
-- Modelo:
--   ciudades            — catálogo normalizado de ciudades de salida
--   empresas_origenes   — n:m: una empresa opera desde N ciudades
--   escuelas.ciudad_id  — ciudad real del colegio (FK al catálogo)
--   localidad se conserva como display + fallback histórico.
--
-- Empresas existentes quedan sin orígenes hasta que el super_admin las
-- mapee desde el admin. Hasta entonces el filtro las oculta, lo cual es
-- correcto (mejor no mostrar que mostrar mal).

-- =============================================================
-- Catálogo de ciudades
-- =============================================================
create table if not exists ciudades (
  id          bigint generated by default as identity primary key,
  nombre      text not null,
  provincia   text,
  slug        text not null unique,
  created_at  timestamptz default now()
);
create index if not exists ciudades_provincia_idx on ciudades(provincia);

